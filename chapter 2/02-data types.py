a = 1 # a is integer

b = 5.33 # b is float point number

c = "Hello" # c is string

d = False # d is a boolen variable

e = None # e is none type varible

print(a)
print(b)
print(c)
print(d)
print(e)
print(type(a))
print(type(b))
print(type(c))
print(type(d))
print(type(e))
# Output:
# 1
# 5.33
# Hello
# False
# None
# <class 'int'>
# <class 'float'>
# <class 'str'>
# <class 'bool'>
# <class 'NoneType'>
# Note: In Python, we do not need to declare the data type of a variable. It is automatically identified by the value assigned to it.
